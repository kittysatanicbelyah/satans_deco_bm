        if (textureBlock.getId().getPath().contains("waxed_")) textureBlock = BMSupGen.getDecoNonWaxed(textureBlock);

        int num1 = (Integer.parseInt(textureBlock.getId().getPath().replaceAll("[^\\d]", "")) * 4) - 3;
